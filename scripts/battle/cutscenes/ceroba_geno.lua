return {
    intro = function(cutscene, battler, enemy)
        local ceroba = Game.battle:getEnemyBattler("ceroba_geno")
        ceroba:getActiveSprite():setAnimation("intro")
        cutscene:wait(2.5)
        ceroba:toggleOverlay(false)
        cutscene:battlerText(ceroba, "...")
        cutscene:battlerText(ceroba, "...")
        cutscene:battlerText(ceroba, "Why...")
        cutscene:battlerText(ceroba, "Why have you\ndone this?")
        cutscene:battlerText(ceroba, "Is it because\nof some personal\ncrusade?")
        cutscene:battlerText(ceroba, "Your species already\nimprisoned us here.\nYou won.")
        cutscene:battlerText(ceroba, "I don't understand...")
        cutscene:battlerText(ceroba, "...")
        cutscene:battlerText(ceroba, "Chujin was right\nall along.")
        cutscene:battlerText(ceroba, "Humans want to rule\nover us;\ncontrol our lives...")
        cutscene:battlerText(ceroba, "Even if we freed\nourselves, they would\nonly wage war again.")
        cutscene:battlerText(ceroba, "If only I stayed\nhere and didn't\nrun off, I could've\nprotected St-")
        cutscene:battlerText(ceroba, "Enough talk.")
        cutscene:battlerText(ceroba, "Starlo was the...\nthe only friend\nI had left.")
        cutscene:battlerText(ceroba, "What you've done\nis unforgivable.")
        cutscene:battlerText(ceroba, "... ")
        cutscene:battlerText(ceroba, "You WILL know the\npain you have caused.")

        cutscene:after(function()
            Game.battle:setState("ACTIONSELECT")
        end, true)
    end,
    death = function(cutscene, battler, enemy)
        Game.battle.music:stop()
        local ceroba = Game.battle:getEnemyBattler("ceroba_geno")
        cutscene:wait(1.5)
        ceroba:getActiveSprite():setAnimation("death")
        cutscene:wait(2.5)
        --ceroba:toggleOverlay(false)
        cutscene:battlerText(ceroba, "...")
        cutscene:battlerText(ceroba, "Look.")
        cutscene:battlerText(ceroba, "Look at me, coward!")
        cutscene:battlerText(ceroba, "The fruits of your\nlabor.")
        cutscene:battlerText(ceroba, "Does this make\nyou happy?")
        cutscene:battlerText(ceroba, "Maybe if I were to\nbeg for mercy.")
        cutscene:battlerText(ceroba, "Make you feel bad\nabout your actions?")
        cutscene:battlerText(ceroba, "No...")
        cutscene:battlerText(ceroba, "...")
        cutscene:battlerText(ceroba, "Once you wipe\nus out...")
        cutscene:battlerText(ceroba, "...I hope you choke on\nthe dust that will\nfill the air.")
        cutscene:battlerText(ceroba, "...")
        cutscene:battlerText(ceroba, "...In the meantime...")
        cutscene:battlerText(ceroba, "Go to Hell.")

        ceroba:onDefeatVaporized(damage, battler)
        --Game.battle.battle_ui:endAttack()
        cutscene:after(function()
            Game.battle:setState("VICTORY")
        end, true)
    end,
    phase_switch = function(cutscene, battler, enemy)
        Game.battle.music:stop()
        local ceroba = Game.battle:getEnemyBattler("ceroba_geno")
        cutscene:wait(0.2)
        ceroba:toggleOverlay(true)
        ceroba:getActiveSprite():setAnimation("phase_switch")
        Assets.playSound("staff_spin")
        cutscene:wait(2)
        Assets.playSound("ceroba_yell")
        Game.battle.music:play("undertale_yellow/trial_by_fury_2")
        cutscene:wait(2)
        ceroba:setActor("cerobaenemy_p2")
        Game.battle:setState("DEFENDINGEND")
        cutscene:wait(0.5)

        cutscene:after(function()
            Game.battle:setState("ACTIONSELECT")
        end, true)
    end
}